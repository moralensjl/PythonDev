# Operadores aritmeticos
print("Suma:", 4 + 3) 
print("Resta:", 4 - 3) 
print("Multiplicacion:", 4 * 3) 
print("Divicion:", 4 / 3) 
print("Division exacta", 4 // 3)
print("Exponente", 4 ** 3)
print("Recordatorio", 10 % 2)

# Operadores de comparacion 
print(4 == 4) # Igual a
print(4 == 3)
print(4 != 3) # distinto a
print(5 <= 3) # menor o igual a 
print(5 >= 4) # mayor o igual
print(6 > 7) # mayor
print(9 < 8) # menor